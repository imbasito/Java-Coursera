public class TenHellos {
    public static void main(String[] args) {

        System.out.println("1st Hello.");
        System.out.println("2nd Hello.");
        System.out.println("3rd Hello.");

        // using for loop.
        for(int i = 4; i<10; i++){
            System.out.println(i+"th" + " Hello.");
        }


        System.out.println("1st Hello.");
        System.out.println("2nd Hello.");
        System.out.println("3rd Hello.");
        // using while loop
        int n = 4; 
        while(n<10){
            System.out.println(n+"th" + " Hello.");
            n++;
        }

    }
}
